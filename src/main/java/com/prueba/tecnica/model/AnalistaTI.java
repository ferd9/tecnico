package com.prueba.tecnica.model;

public class AnalistaTI {
    private Long idAnalista;
    private String nombre;
    private String sistemaEspecialidad;
	/**
	 * @return the idAnalista
	 */
	public Long getIdAnalista() {
		return idAnalista;
	}
	/**
	 * @param idAnalista the idAnalista to set
	 */
	public void setIdAnalista(Long idAnalista) {
		this.idAnalista = idAnalista;
	}
	/**
	 * @return the nombre
	 */
	public String getNombre() {
		return nombre;
	}
	/**
	 * @param nombre the nombre to set
	 */
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	/**
	 * @return the sistemaEspecialidad
	 */
	public String getSistemaEspecialidad() {
		return sistemaEspecialidad;
	}
	/**
	 * @param sistemaEspecialidad the sistemaEspecialidad to set
	 */
	public void setSistemaEspecialidad(String sistemaEspecialidad) {
		this.sistemaEspecialidad = sistemaEspecialidad;
	}
    
}
